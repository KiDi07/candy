#!/bin/bash

# Скрипт для быстрого обновления Candy Bot

echo "--- Начинаю обновление проекта ---"

# Подтягиваем изменения из Git
echo "1. Получение изменений из Git..."
git pull

# Пересобираем и перезапускаем контейнеры
echo "2. Пересборка Docker образа и перезапуск..."
docker compose up -d --build

# Удаляем старые неиспользуемые образы, чтобы не забивать место
echo "3. Очистка старых образов..."
docker image prune -f

echo "--- Обновление завершено! ---"
echo "Для просмотра логов используйте: docker compose logs -f"
